package ch03;
//부정연산자
public class Ex06NotOperator {

	public static void main(String[]args) {
		boolean result = true;
		System.out.printf("result = %b%n", result);
		System.out.printf("!result = %b%n", !result);
		System.out.printf("!!result = %b%n", !!result);
		System.out.printf("!!!result = %b%n", !!!result);
		
	}
}
